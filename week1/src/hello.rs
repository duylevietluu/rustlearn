fn main() {
    println!("Hello, world!");
    let day = 23;
    let month = 8;

    println!("My birth month is {}", month);
    println!("The day is {:8}", day);

    let pi = 3.141592;
    println!("Pi is roughly {}", pi);
    println!("Rounded, pi = {:.2}", pi);
    println!("Or, pi is {:10.4}", pi);
}